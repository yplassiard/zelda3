// Returns pointer to 8-byte glyph for a UTF-8 accented char, or NULL.
// Call with the second byte of a 0xC3 xx UTF-8 sequence.
static inline const uint8_t *GetAccentedGlyph(uint8_t second_byte) {
  if (second_byte < 0x80 || second_byte > 0xBF) return NULL;
  const uint8_t *g = kFontAccented[second_byte - 0x80];
  // Check if glyph is defined (non-zero)
  if (g[0] == 0 && g[1] == 0 && g[2] == 0 && g[3] == 0) return NULL;
  return g;
}

